# Exercicio 17
# A formula de calculo de área de um circulo é:
# 
# area = pi*r²
# 
# Sabemos que:
# 
# pi = 3.14
# r = raio da circunferência em metros (float)
# 
# Crie um programa que peça ao usuário o raio e calcule a área da circunferência
# 
valorRaio = float(input("Digite o valor do raio da circunferência: "))
area = 3.14 * (valorRaio * valorRaio)
print("A área da circunferência é ", area)