# Exercicio 14
# Crie um programa que peça ao usuário digitar um número de 1 a 7 e mostre o dia da semana correspondente sendo que segunda feira é o numero 1 e domingo é 7.

numero = str(input("Digite um número de 1 a 7: "))
if (numero == '1'):
    print("Segunda-Feira")
if (numero == '2'):
    print("Terça-Feira")
if (numero == '3'):
    print("Quarta-Feira")
if (numero == '4'):
    print("Quinta-Feira")
if (numero == '5'):
    print("Sexta-Feira")
if (numero == '6'):
    print("Sábado")
if (numero == '7'):
    print("Domingo")


